/**
 * Get suggestions for a given field context key.
 *
 * @param {string} contextKey  e.g. 'capability', 'menuIcon', 'postType'
 * @param {string} query       Current input value for filtering.
 * @returns {string[]}
 */
export const getSuggestions = (contextKey, query = '') => {
  const list = WP_CONTEXT_MAP[contextKey] || [];
  if (!query) return list;
  const q = query.toLowerCase();
  return list.filter((s) => s.toLowerCase().includes(q));
};
